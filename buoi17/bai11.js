document.write(Math.abs(-5.5));
document.write(Math.abs(10));

document.write(Math.ceil(4.7));
document.write(Math.ceil(5.2));

document.write(Math.floor(4.7));
document.write(Math.floor(5.2));

document.write(Math.round(4.7));
document.write(Math.round(5.2));

/*
math.abs là câu lệnh đưa giá trị số vào trị tuyệt đối bỏ dấu nên -5.5 đổi sang 5.5 còn 10 thì giữ nguyên 
Math.ceil là câu lệnh làm cho các chữ số thập phân được làm tròn lên làm cho 4.7 thành 5 5.2 thành 6
math.floor thì ngược lại với ceil nó làm tròn các số xuống 4.7 thành 4 5.2 thành 5
math.round làm tròn với mộc số thập phân là x.5 nếu phần thập phân lớn hơn hoặc bằng 5 thì sẽ làm tròn lên nếu nhỏ hơn 5 thì sẽ làm tròn xuống 
*/
